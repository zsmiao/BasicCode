package com.itheima.method;

class Demo01Method {
    public static void main(String[] args) {
        study();
        eat();
    }

    public static void eat() {
        System.out.println("吃饭睡觉打豆豆！！");
    }

    public static void study() {
        for (int i = 0; i <5 ; i++) {
            System.out.println("只要学不死，就往死里学");
        }
    }
}
